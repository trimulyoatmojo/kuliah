public class Titik { 
private int x, y;
public Titik() {
        x=3;
        y=2;
    }
    public Titik( int x1, int y1) {
        x=x1;
        y=y1;
    }
    public void setx(int x1) {
        x=x1;
    }
    public void sety(int y1) {
        y=y1;
    }
    public int getx () {
        return x;
    }
    public int gety () {
        return y;
    }
} 