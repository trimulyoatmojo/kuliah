public class Tugas1 {
    public static void main (String argv[]) {
        int m,n;
        n = 1;
        m = 0;
        while ( n <= 100 ) {
            m = m + n;
            n++;
        }
    System.out.println ("Program menghitung jumlah bilangan 1 sampai dengan 100");
    System.out.println ("Nilai (1+2+3+4+...+100) = " + m);
    }
} 